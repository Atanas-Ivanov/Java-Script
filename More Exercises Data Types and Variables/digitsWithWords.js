function digitsWithWords(word) {
	switch (word) {
		case 'one':
			if (word == 'one') {
				console.log(1);
			}
			break;
		case 'two':
			if (word == 'two') {
				console.log(2);
			}
			break;
		case 'three':
			if (word == 'three') {
				console.log(3);
			}
			break;
		case 'four':
			if (word == 'four') {
				console.log(4);
			}
			break;
		case 'five':
			if (word == 'five') {
				console.log(5);
			}
			break;
		case 'six':
			if (word == 'six') {
				console.log(6);
			}
			break;
		case 'seven':
			if (word == 'seven') {
				console.log(7);
			}
			break;
		case 'eight':
			if (word == 'eight') {
				console.log(8);
			}
			break;
		case 'nine':
			if (word == 'nine') {
				console.log(9);
			}
			break;
	}
}
digitsWithWords('two');
